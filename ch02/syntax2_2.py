# round()
price = 1.83
rate = 0.55
tax = round(price * rate, 2)
print('tax is', tax)

# min()
price1 = 1.67
price2 = 0.56
price3 = 2.99
price4 = 0.19
price5 = 1.25
best = min(price1, price2, price3, price4, price5)
print('best price is', best)

# max()
worst = max(price1, price2, price3, price4, price5)
print('worst price is', worst)

